package com.pax.busi.common.entity;

public class Chintoeng {
	
	private String	rspcode;
	private String	chinmsg;
	private String	engmsg;
	private String	status;
	
	public String getRspcode() {
		return rspcode;
	}
	
	public void setRspcode(String rspcode) {
		this.rspcode = rspcode;
	}
	
	public String getChinmsg() {
		return chinmsg;
	}
	
	public void setChinmsg(String chinmsg) {
		this.chinmsg = chinmsg;
	}
	
	public String getEngmsg() {
		return engmsg;
	}
	
	public void setEngmsg(String engmsg) {
		this.engmsg = engmsg;
	}
	
	public String getStatus() {
		return status;
	}
	
	public void setStatus(String status) {
		this.status = status;
	}
	
}
